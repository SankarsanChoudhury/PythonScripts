#Tuples are immutable. You cant change the value of tuples
tup=(21,36,41,45)
print(tup)
print(tup[1])
#Count() method will find the no of occurrences
#Tuple is used where we dont want to change the values.Iteration is fast in tuple as compared to list.
print(tup.count(21))

